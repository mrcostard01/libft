#include "libft.h"

void	*ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t	i;
	int		*p;

	i = 0;
	p = 0;
	n--;
	while (i < n)
	{
		if (*(char *)s1 != *(char *)s2)
		{
			*p = *(char *)s1 - *(char *)s2;
			return (p);
		}
		s1++;
		s2++;
		i++;
	}
	return (p);
}
