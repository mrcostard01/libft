int	ft_isalnum(int c)
{
	int	istrue;

	if (c == '\0')
		return (1);
	if ((c >= 97 && c <= 122)
		|| (c >= 65 && c <= 91)
		|| (c >= 48 && c <= 57))
		istrue = 1;
	else
	{
		istrue = 0;
		return (istrue);
	}
	return (istrue);
}
