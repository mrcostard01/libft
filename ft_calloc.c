#include "libft.h"
void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*arr;

	if ((nmemb * size) > 214743647)
		return(NULL);
	arr = malloc(nmemb * size);
	if (!arr)
		return(NULL);
	ft_bzero(arr, (nmemb * size));
	return (arr);
}

