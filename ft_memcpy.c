#include "libft.h"

void	*ft_memcpy(void *d, const void *s, size_t n)
{
	size_t	i;

	i = 0;
	n--;
	while (i <= n)
	{
		*(char *)d = *(char *)s;
		s++;
		d++;
		i++;
	}
	return (d);
}
