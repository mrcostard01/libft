int	ft_isprint(int c)
{
	int	istrue;

	if (c == '\0')
		return (1);
	if (c >= 32 && c <= 126)
		istrue = 1;
	else
	{
		istrue = 0;
		return (istrue);
	}
	return (istrue);
}
