#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t	i;
	void	*a;

	i = 0;
	a = 0;
	while (i < n)
	{
		if (*(char *)s == c)
		{
			*(char *)a = *(char *)s;
			return (a);
		}
		s++;
		i++;
	}
	return (0);
}
