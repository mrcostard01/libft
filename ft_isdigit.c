int	ft_isnumeric(int c)
{
	int	istrue;

	if (c == '\0')
		return (1);
	if (c >= 48 && c <= 57)
		istrue = 1;
	else
	{
		istrue = 0;
		return (istrue);
	}
	return (istrue);
}
